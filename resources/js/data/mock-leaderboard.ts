/**
 * Get unique countries from the mock users data
 */
export function getAvailableCountries(): Country[] {
    const countryCodes = [...new Set(MOCK_USERS.map((user) => user.countryCode))];
    return COUNTRIES.filter((country) => countryCodes.includes(country.code)).sort((a, b) => a.name.localeCompare(b.name));
}

/**
 * Get regions for a specific country from mock users
 */
export function getRegionsForCountry(countryCode: string): string[] {
    const country = COUNTRIES.find((c) => c.code === countryCode);
    if (!country) return [];

    // Only return regions that have users
    const userRegions = MOCK_USERS.filter((user) => user.countryCode === countryCode)
        .map((user) => user.region)
        .filter((region): region is string => region !== undefined);

    return [...new Set(userRegions)].sort();
}
